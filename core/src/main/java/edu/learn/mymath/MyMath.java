package edu.learn.mymath;

public class MyMath {
    public float sum(int n1, int n2){
        return (float) n1 + n2;
    }

    public float subtract(int n1, int n2){
        return (float) n1 - n2;
    }

    public float multiply(int n1, int n2){
        return (float) n1 * n2;
    }

    public float divide(int n1, int n2){
        return (float) n1 / n2;
    }
}
